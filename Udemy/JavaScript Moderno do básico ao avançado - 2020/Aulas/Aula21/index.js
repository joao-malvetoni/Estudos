/**
 * Operadores de comparação
 * > maior que
 * >= maior que ou igual
 * < menor que
 * <= menor que ou igual
 * == igualdade (valor) (não recomendado)
 * === igualdade estrita (valor e tipo) (recomendado)
 * != diferente (valor) (não recomendado)
 * !== diferente estrito (valor e tipo) (recomendado)
 */

 console.log(10 > 5);
 console.log(10 >= 5);
 console.log(10 < 11);
 console.log(10 <= 11);
 console.log(10 == '10');
 console.log(10 === 10);
 console.log(10 != '10');
 console.log(10 !== '10');