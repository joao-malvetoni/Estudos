/**
 * Operadores lógicos
 * && -> AND -> E -> Todas as expressões precisam ser verdadeiras para retornar true
 * || -> OR -> OU -> 
 * ! -> NOT -> NÃO ->
 */

 console.log(true && true && true && true);
 console.log(true && true && false && true);

 console.log(true || true || true || true);
 console.log(false || false || true || false);

 console.log(!true);
 console.log(!false);